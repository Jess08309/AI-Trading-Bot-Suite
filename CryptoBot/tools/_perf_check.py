print("=" * 60)
print(f"  Status:         {ps.get('status','?')}")
print(f"  Position Count: {ps.get('position_count',0)} ({ps.get('call_count',0)} calls, {ps.get('put_count',0)} puts)")
print(f"  Total Credit:   ")
print(f"  Total Risk:     ")
print(f"  Unrealized PnL: ")
print(f"  Closed Trades:  {ps.get('total_trades',0)}")
