# Per-position summary
positions = ps.get('positions', [])
print(f"\n  --- Open Positions ({len(positions)}) ---")
winners = sum(1 for p in positions if p.get('current_pnl',0) > 0)
losers = sum(1 for p in positions if p.get('current_pnl',0) < 0)
flat = sum(1 for p in positions if p.get('current_pnl',0) == 0)
total_pnl = sum(p.get('current_pnl',0) for p in positions)
worst = min(positions, key=lambda p: p.get('current_pnl_pct',0)) if positions else {}
best = max(positions, key=lambda p: p.get('current_pnl_pct',0)) if positions else {}
print(f"  Green/Red/Flat:  {winners}/{losers}/{flat}")
print(f"  Net Unrealized:  ")
if worst:
    print(f"  Worst position:  {worst['underlying']} {worst['spread_type']}  ({worst.get('current_pnl_pct',0):+.1f}%)")
if best:
    print(f"  Best position:   {best['underlying']} {best['spread_type']}  ({best.get('current_pnl_pct',0):+.1f}%)")
